#capitalizing string
name = "Tony Stark"
print(name.upper())

#lower case string
name = "Tony Stark"
print(name.lower())

#finding an element in the string
name = "Tony Stark"
print(name.find('S'))

#replace string with other
name = "Tony Stark"
print(name.replace('Tony Stark', 'Ironman'))

#keyword, finding if the element is there or not
name = "Tony Stark"
print('T' in name)


